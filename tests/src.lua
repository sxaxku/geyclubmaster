print("--- 1. Числовой цикл for ---")

-- Задаем переменные для цикла
local a = 1   -- Начало
local b = 5   -- Конец
local c = 1   -- Шаг

-- Ваш код:
for i = a, b, c do
    -- Выводит: текущий счетчик, старт, стоп, шаг
    print("i="..i, "a="..a, "b="..b, "c="..c)
end


-- === ЧАСТЬ 2: Итерация по таблице через pairs ===
print("\n--- 2. Цикл for через pairs ---")

-- Переопределяем 'b' как таблицу для этого примера
local b = {
    apple = "red",
    banana = "yellow",
    [1] = "number index"
}

-- Ваш код:
for i, a in pairs(b) do
    -- i - ключ, a - значение (по вашему коду), b - сама таблица
    print("Key:", i, "Value:", a, "Table:", b)
end


-- === ЧАСТЬ 3: Низкоуровневая итерация через next ===
print("\n--- 3. Цикл for через next (аналог pairs) ---")

-- Для этого цикла нам нужна переменная 'd' (начальный индекс)
-- Обычно это nil, чтобы начать с начала таблицы
local d = nil 

-- Ваш код (исправлена запятая после d):
-- next - функция-итератор, b - инвариантное состояние (таблица), d - управляющая переменная
for i, a in next, b, d do
    -- i - ключ, a - значение, b - таблица, d - начальная точка (nil)
    print("Key:", i, "Value:", a, "Table:", b, "Start:", d)
end